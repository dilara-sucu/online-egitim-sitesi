                                                <h5>Güzel Bir Türkçe Kullanın</h5>

                                                <p>Sorunun anlaşılabilir olması, cevap bulabilmesi için çok önemli. Sorularınızı düzgün bir Türkçe ile anlaşılır şekilde yazılmış olması cevap bulmanızı kolaylaştıracak, yanlış anlamalardan doğabilecek zaman kayıplarını engelleyecektir.</p>

                                                <h5>Açık Fikirli Olun</h5>

                                                <p>Sorunuza tam olarak istediğiniz cevabı alamamış olabilirsiniz. Farklı çözüm yolları önerilerine, farklı bakış açılarına açık olun, denemekten ve tartışmaktan çekinmeyin.</p>

                                                <h5>İşlev Setlerini Kullanın</h5>

